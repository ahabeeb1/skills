#!/usr/bin/env bash
# habeebs-skill — generate the Codex Agent Skills tree from the canonical source.
#
# Per the dual-native parity ADR (2026-06-25), skills/ is the SINGLE SOURCE OF
# TRUTH. The Codex discovery tree at .agents/skills/ is GENERATED from it, never
# hand-edited. Codex CLI discovers skills at $REPO_ROOT/.agents/skills/<name>/SKILL.md
# (progressive disclosure: only name+description load at startup), so we mirror
# each canonical skill directory there verbatim.
#
# Deterministic: same skills/ input -> byte-identical .agents/skills/ output.
# Only directories that contain a SKILL.md are mirrored (the cross-session-detect
# library has no SKILL.md and is intentionally skipped — it ships with the hooks,
# not as a Codex-invocable skill).
#
# Usage:
#   bash bin/sync-codex.sh            regenerate .agents/skills/ in place
#   bash bin/sync-codex.sh --check    regenerate into a temp dir, diff against the
#                                     committed tree, exit 1 on drift (CI gate)

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
SRC="$REPO_ROOT/skills"
DEST="$REPO_ROOT/.agents/skills"

GENERATED_NOTICE='<!-- GENERATED by bin/sync-codex.sh from skills/ — do not edit. Edit the canonical skill, then run: bash bin/sync-codex.sh -->'

generate() {
  local dest="$1"
  rm -rf "$dest"
  mkdir -p "$dest"

  # Mirror each canonical skill dir (those with a SKILL.md), in sorted order for
  # determinism. cp -R preserves the references/ and scripts/ subtrees as-is.
  local d name
  for d in "$SRC"/*/; do
    [ -f "${d}SKILL.md" ] || continue
    name="$(basename "$d")"
    mkdir -p "$dest/$name"
    cp -R "$d." "$dest/$name/"
  done

  # Static generated-marker README (no timestamps -> deterministic output).
  cat > "$dest/README.md" <<EOF
# Codex Agent Skills (generated)

$GENERATED_NOTICE

This directory is the Codex CLI discovery tree for habeebs-skill. It is a
generated mirror of the canonical \`skills/\` tree at the repository root.

- **Do not edit files here.** Edit the canonical skill under \`skills/<name>/\`,
  then regenerate: \`bash bin/sync-codex.sh\`.
- Drift between this tree and \`skills/\` is a CI failure
  (\`tests/codex/02-skill-drift/check-drift.sh\`).
- Codex discovers these at \`\$REPO_ROOT/.agents/skills/<name>/SKILL.md\` and
  invokes a skill by name with \`\$<skill-name>\`.
EOF
}

case "${1:-}" in
  --check)
    tmp="$(mktemp -d)"
    trap 'rm -rf "$tmp"' EXIT
    generate "$tmp/skills"
    if diff -r "$DEST" "$tmp/skills" >/tmp/codex-drift.$$ 2>&1; then
      echo "OK: .agents/skills/ is in sync with skills/"
      exit 0
    else
      echo "DRIFT: .agents/skills/ is stale. Run: bash bin/sync-codex.sh" >&2
      echo "--- diff (committed vs freshly generated) ---" >&2
      cat /tmp/codex-drift.$$ >&2
      rm -f /tmp/codex-drift.$$
      exit 1
    fi
    ;;
  ""|--write)
    generate "$DEST"
    count="$(find "$DEST" -mindepth 1 -maxdepth 1 -type d | wc -l | tr -d ' ')"
    echo "Generated .agents/skills/ — $count skills mirrored from skills/."
    ;;
  *)
    echo "usage: bash bin/sync-codex.sh [--check]" >&2
    exit 2
    ;;
esac
